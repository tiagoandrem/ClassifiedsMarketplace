        MysqlDBWorker dbWorker = new MysqlDBWorker();
        
        ment = new MoradaDeEntrega(txt_nome.getText(), txt_rua.getText(), txt_localidade.getText(), txt_codigoPostal.getText(), txt_contacto.getText(), txt_infoAdicional.getText());
       

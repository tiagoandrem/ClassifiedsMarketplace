    public PaginaInicial() {
        initComponents();
        
        MysqlDBWorker slave = new MysqlDBWorker();
        MostrarProduto d = new MostrarProduto(); 
        
        try {
            produtos = slave.getAllProducts();
        } catch (SQLException ex) {
            Logger.getLogger(PaginaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        for (Produto produto : produtos) {
            LabelProduto labelProduto = new LabelProduto();
            labelProduto.setProduto(produto);
            
            labelProduto.addMouseListener(new MouseAdapter()  
            {
                public void mouseClicked(MouseEvent e){
                    DlgMostraProduto indice = new DlgMostraProduto(new javax.swing.JFrame(), true, produto, utilizador);
                    indice.setVisible(true);       
                }
            });  
            pnlAnuncios.add(labelProduto);   
        }
    }

